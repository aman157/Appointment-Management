package org.mz.modal;

public class IdCardSendModal extends EnrolledModal{
	
	private String timing;
	private String trainer;
	private String training;
	private String demoDate1;
	private String demoDate2;
	private String demoDate3;
	private String emailStatus;
	
	public String getTiming() {
		return timing;
	}
	public void setTiming(String timing) {
		this.timing = timing;
	}
	public String getTrainer() {
		return trainer;
	}
	public void setTrainer(String trainer) {
		this.trainer = trainer;
	}
	public String getTraining() {
		return training;
	}
	public void setTraining(String training) {
		this.training = training;
	}
	public String getDemoDate1() {
		return demoDate1;
	}
	
	public void setDemoDate1(String demoDate1) {
		this.demoDate1 = demoDate1;
	}
	public String getDemoDate2() {
		return demoDate2;
	}
	public void setDemoDate2(String demoDate2) {
		this.demoDate2 = demoDate2;
	}
	public String getDemoDate3() {
		return demoDate3;
	}
	public void setDemoDate3(String demoDate3) {
		this.demoDate3 = demoDate3;
	}
	
	public String getEmailStatus() {
		return emailStatus;
	}
	public void setEmailStatus(String emailStatus) {
		this.emailStatus = emailStatus;
	}
	
}
